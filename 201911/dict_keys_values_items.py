#keys() 方法用于返回字典中的所有键；values() 方法用于返回字典中所有键对应的值；items() 用于返回字典中所有的键值对。
dict= {'数学': 95, '语文': 89, '英语': 90}
print(dict)
print(dict.keys())
print(dict.values())
print(dict.items())

print(type(dict.keys()))
print(type(dict.values()))
print(type(dict.items()))